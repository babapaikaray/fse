package com.baba.crud.model;

public class DeductionReport {
	 Float startingSal;
	 String noOfDeductions;
	 Float deductionPer;
	 Float deductionAmt;
	public Float getStartingSal() {
		return startingSal;
	}
	public void setStartingSal(Float startingSal) {
		this.startingSal = startingSal;
	}
	public String getNoOfDeductions() {
		return noOfDeductions;
	}
	public void setNoOfDeductions(String noOfDeductions) {
		this.noOfDeductions = noOfDeductions;
	}
	public Float getDeductionPer() {
		return deductionPer;
	}
	public void setDeductionPer(Float deductionPer) {
		this.deductionPer = deductionPer;
	}
	public Float getDeductionAmt() {
		return deductionAmt;
	}
	public void setDeductionAmt(Float deductionAmt) {
		this.deductionAmt = deductionAmt;
	}
	 
	 
}
