package com.baba.crud.model;

public class PredictionReport {

	 Float startingSal;
	 Float incrementAmt;
	 Float deductiontAmt;
	 Float growthPer;
	public Float getStartingSal() {
		return startingSal;
	}
	public void setStartingSal(Float startingSal) {
		this.startingSal = startingSal;
	}
	public Float getIncrementAmt() {
		return incrementAmt;
	}
	public void setIncrementAmt(Float incrementAmt) {
		this.incrementAmt = incrementAmt;
	}
	public Float getDeductiontAmt() {
		return deductiontAmt;
	}
	public void setDeductiontAmt(Float deductiontAmt) {
		this.deductiontAmt = deductiontAmt;
	}
	public Float getGrowthPer() {
		return growthPer;
	}
	public void setGrowthPer(Float growthPer) {
		this.growthPer = growthPer;
	}
	 
	 
}
